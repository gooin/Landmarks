import SwiftUI

struct LandmarkList: View {
    var body: some View {
        NavigationView {
            List(landmarks) { landmark in
                NavigationLink(destination: LandmarkDetail(landmark: landmark)){
                    LandmarksRow(landmark: landmark)
                }
            }
            .navigationTitle("Landmarks")
        }
       
    }
}

